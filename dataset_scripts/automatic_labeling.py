LBL_TRAIN = os.path.join(YOLO_DATASET, "labels", "train")
UNLABELED_DIR = os.path.join(YOLO_DATASET, "images", "unlabeled_images")

os.makedirs(IMG_TRAIN, exist_ok=True)
os.makedirs(LBL_TRAIN, exist_ok=True)
os.makedirs(UNLABELED_DIR, exist_ok=True)

# ================= SINGLE CLASS =================
CLASS_NAME = "pulley"
CLASS_ID = 0

# ================= MODELS =================
YOLO_MODEL_PATH = "yolov8n.pt"
SAM_CHECKPOINT = "/content/drive/MyDrive/100_parts_images/sam_vit_h_4b8939.pth"

yolo = YOLO(YOLO_MODEL_PATH)

sam = sam_model_registry["vit_h"](checkpoint=SAM_CHECKPOINT).to(DEVICE)
sam_predictor = SamPredictor(sam)

# ================= PROCESS IMAGE =================
def process_image(image_path, output_name):
    image = cv2.imread(image_path)
    if image is None:
        return False

    h, w = image.shape[:2]
    image_rgb = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

    result = yolo(
        image,
        conf=CONF_THRESHOLD,
        device=DEVICE,
        agnostic_nms=True,
        verbose=False
    )[0]

    if result.boxes is None:
        return False

    sam_predictor.set_image(image_rgb)
    label_lines = []

    for box in result.boxes.xyxy.cpu().numpy():
        x1, y1, x2, y2 = map(int, box)
        x1, y1 = max(0, x1), max(0, y1)
        x2, y2 = min(w - 1, x2), min(h - 1, y2)

        masks, _, _ = sam_predictor.predict(
            box=np.array([x1, y1, x2, y2]),
            multimask_output=False
        )

        mask = masks[0].astype(np.uint8)
        contours, _ = cv2.findContours(
            mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE
        )

        if not contours:
            continue

        cnt = max(contours, key=cv2.contourArea)
        if cv2.contourArea(cnt) < MIN_MASK_AREA:
            continue

        epsilon = 0.005 * cv2.arcLength(cnt, True)
        approx = cv2.approxPolyDP(cnt, epsilon, True)
        polygon = approx.reshape(-1, 2)

        if polygon.shape[0] < 3:
            continue

        poly = Polygon(polygon)
        if not poly.is_valid:
            continue

        norm_poly = []
        for x, y in polygon:
            norm_poly.append(x / w)
            norm_poly.append(y / h)

        label_lines.append(
            f"{CLASS_ID} " + " ".join(map(str, norm_poly))
        )

    if not label_lines:
        return False

    # write label
    with open(os.path.join(LBL_TRAIN, f"{output_name}.txt"), "w") as f:
        f.write("\n".join(label_lines))

    # copy image
    shutil.copy(
        image_path,
        os.path.join(IMG_TRAIN, f"{output_name}.jpg")
    )

    return True

