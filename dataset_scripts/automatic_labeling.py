# ================= CONFIG =================
CONF_THRESHOLD = 0.1
MIN_MASK_AREA = 150
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

# ================= DATASET PATHS =================
DATASET_ROOT = "/content/drive/MyDrive/100_parts_images/screw_balanced_100/screw"
